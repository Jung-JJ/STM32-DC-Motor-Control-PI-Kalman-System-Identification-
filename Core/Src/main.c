/**
  * @brief  Function implementing the MotorControlTas thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL);
	HAL_GPIO_WritePin(AN1_GPIO_Port, AN1_Pin, 0);
	HAL_GPIO_WritePin(AN2_GPIO_Port, AN2_Pin, 1);
	last_cnt = __HAL_TIM_GET_COUNTER(&htim3);
	while(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_RESET) {
	      osDelay(10);
	  }
	last_cnt = __HAL_TIM_GET_COUNTER(&htim3);
	u_prev = 0; // 혹시 모를 잔여 출력값도 초기화
	e_prev = 0; // 이전 오차도 초기화
  /* Infinite loop */
  for(;;)
  {
	  // 1. 피드백: 현재 속도 읽기
	        int32_t current_cnt = __HAL_TIM_GET_COUNTER(&htim3);
	        int16_t delta_tick = (int16_t)(current_cnt - last_cnt);
	        last_cnt = current_cnt;
	        raw_v = ((float)delta_tick / 1632.0f) * 0.2041f * 100.0f;
	        current_v = Kalman_Filter(raw_v);
	        // 2. 오차 계산
	        float e = setpoint - current_v;

	        // 3. PI 제어 연산 (Tustin 수식 적용(PI 제어기 적용))
	        float u_out = u_prev + 1.2f*(491.16f * e) - (423.87f * e_prev);

	        // 4. 출력 제한 (TIM1 ARR이 5400이므로)
	        if (u_out > 5400.0f) u_out = 5400.0f;
	        if (u_out < 0.0f) 		   u_out = 0.0f;

	        // 5. PWM 출력
	        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint32_t)u_out);

	        // 6. 상태 업데이트
	        u_prev = u_out;
	        e_prev = e;

	        osDelay(10);
  /* USER CODE END 5 */
  }
}

