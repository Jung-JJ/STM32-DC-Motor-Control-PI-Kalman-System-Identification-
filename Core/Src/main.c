/* USER CODE BEGIN 4 */
float Kalman_Filter(float z) {
    P = P + Q;
    float K = P / (P + R);
    x_hat = x_hat + K * (z - x_hat);
    P = (1.0f - K) * P;
    return x_hat;
}
